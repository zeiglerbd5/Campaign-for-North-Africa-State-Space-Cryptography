# ---------------------------------------------------------------------------
# Vassal hex grid parameters  (buildFile.xml HexGrid element)
# ---------------------------------------------------------------------------
HEX_DX       = 72.95
HEX_DY       = 85.25
HEX_X0       = -15.0
HEX_Y0       =   4.0
HEX_SIDEWAYS = True   # flat-top hexes (sideways=true in Vassal)

# ---------------------------------------------------------------------------
# On-map hex snap positions  (4,959 positions covering the main CNA map)
# Each entry: (col, row, pixel_x, pixel_y)
# col: 1-174 (west to east),  row: 1-29 (north to south)
# ---------------------------------------------------------------------------
HEX_POSITIONS: list[tuple[int, int, int, int]] = []
for _c in range(1, 175):
    for _r in range(1, 35):
        _px = HEX_X0 + _c * HEX_DX
        _py = HEX_Y0 + _r * HEX_DY + (_c % 2) * (HEX_DY / 2)
        if 0 <= _px <= 13000 and 0 <= _py <= 2500:
            HEX_POSITIONS.append((_c, _r, round(_px), round(_py)))

# ---------------------------------------------------------------------------
# Off-map named zones  (19 holding boxes and in-transit areas)
