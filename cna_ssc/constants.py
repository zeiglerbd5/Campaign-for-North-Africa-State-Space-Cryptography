# ---------------------------------------------------------------------------
# Combined location vocabulary
# ---------------------------------------------------------------------------
LOCATIONS: list[dict] = []
for (_c, _r, _px, _py) in HEX_POSITIONS:
    LOCATIONS.append({"type": "hex", "col": _c, "row": _r, "px": _px, "py": _py})
for (_zn, _cx, _cy) in OFFMAP_ZONES:
    LOCATIONS.append({"type": "zone", "name": _zn, "px": _cx, "py": _cy})
LOCATIONS.append({"type": "eliminated"})

NUM_LOCATIONS    = len(LOCATIONS)  # 4979
ELIMINATED_INDEX = NUM_LOCATIONS - 1

# Reverse lookup dictionaries
_LOC_BY_PIXEL: dict[tuple[int,int], int] = {
    (loc["px"], loc["py"]): i
    for i, loc in enumerate(LOCATIONS) if "px" in loc
}
_LOC_BY_ZONE: dict[str, int] = {
    loc["name"]: i
    for i, loc in enumerate(LOCATIONS) if loc.get("type") == "zone"
}


def nearest_location_index(px: int, py: int) -> int:
    """Return index of nearest valid location to given pixel coordinates."""
    key = (px, py)
    if key in _LOC_BY_PIXEL:
        return _LOC_BY_PIXEL[key]
    best_i, best_d = ELIMINATED_INDEX, float("inf")
    for i, loc in enumerate(LOCATIONS):
        if "px" not in loc:
            continue
        d = (loc["px"] - px) ** 2 + (loc["py"] - py) ** 2
        if d < best_d:
            best_d, best_i = d, i
    return best_i


def zone_index(name: str) -> int:
    """Return location index for a named off-map zone."""
    return _LOC_BY_ZONE.get(name, ELIMINATED_INDEX)


