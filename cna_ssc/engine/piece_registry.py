_RAW: List[Dict] = []

def _load() -> List[Dict]:
    """Load piece registry from embedded data or adjacent JSON file."""
    global _RAW
    if _RAW:
        return _RAW
    # Try to load from the JSON file (generated by buildfile_parser.py)
    here = os.path.dirname(os.path.abspath(__file__))
    json_path = os.path.join(here, "piece_registry_data.json")
    if os.path.exists(json_path):
        with open(json_path) as f:
            _RAW = json.load(f)
    else:
        raise FileNotFoundError(
            f"Piece registry data not found at {json_path}. "
            "Run: python -m cna_ssc.formats.buildfile_parser <buildFile.xml>"
        )
    return _RAW


