def get_valid_locations(cat: str) -> List[str]:
    """Return ordered list of valid location IDs for a piece category.

    DESIGN NOTE: Only hex positions are used in the bijection.
    Off-map zones are excluded because their pixel coordinates overlap
    with playable map hexes, making zone→pixel→zone roundtrips unreliable.
    With ~4,959 hex positions per piece, the state space remains enormous
    (4959^187 ≈ 10^654) while eliminating any ambiguity.

    The list order is canonical — do not change once established.
    """
    # All mobile pieces: any valid hex position
    if cat in ("ground_unit", "aircraft", "airfield_naval", "marker"):
        return HEX_POSITIONS

    if cat == "tracker":
        return list(_TRACKER_LOCATIONS) or HEX_POSITIONS

    if cat == "weather":
        return list(_WEATHER_LOCATIONS) or HEX_POSITIONS

    if cat == "label":
        return list(_FIXED_LOCATIONS) or HEX_POSITIONS

    # fallback
    return HEX_POSITIONS


# ──────────────────────────────────────────────────────────────────────────────
# Public API
# ──────────────────────────────────────────────────────────────────────────────

class Piece:
    """Lightweight piece descriptor."""
    __slots__ = ("name", "gpid", "image", "cat", "proto",
                 "valid_locations", "_loc_index")

    def __init__(self, raw: Dict):
        self.name   = raw["name"]
        self.gpid   = raw["gpid"]
        self.image  = raw.get("image", "")
        self.cat    = raw.get("cat", "unknown")
        self.proto  = raw.get("proto", "")
        self.valid_locations: List[str] = get_valid_locations(self.cat)
        self._loc_index: Dict[str, int] = {
            loc: i for i, loc in enumerate(self.valid_locations)
        }

    @property
    def n_positions(self) -> int:
        """Radix for this piece in the mixed-radix encoding."""
        return len(self.valid_locations)

    def location_index(self, loc_id: str) -> int:
        """Return the index of loc_id in this piece's valid_locations list."""
        try:
            return self._loc_index[loc_id]
        except KeyError:
            raise ValueError(
                f"Location {loc_id!r} is not valid for piece {self.name!r} "
                f"(cat={self.cat})"
            )

    def __repr__(self) -> str:
        return f"<Piece {self.name!r} cat={self.cat} n_pos={self.n_positions}>"


# Piece list and lookup tables — populated lazily
_PIECES: Optional[List[Piece]] = None
_PIECE_BY_NAME: Optional[Dict[str, Piece]] = None
_PIECE_BY_GPID: Optional[Dict[str, Piece]] = None


def _ensure_loaded():
    global _PIECES, _PIECE_BY_NAME, _PIECE_BY_GPID
    if _PIECES is not None:
        return
    raw = _load()
    _PIECES = [Piece(r) for r in raw]
    _PIECE_BY_NAME = {p.name: p for p in _PIECES}
    _PIECE_BY_GPID = {p.gpid: p for p in _PIECES}


def pieces() -> List[Piece]:
    """Return the canonical ordered list of all pieces."""
    _ensure_loaded()
    return _PIECES          # type: ignore[return-value]


def piece_by_name(name: str) -> Piece:
    _ensure_loaded()
    return _PIECE_BY_NAME[name]   # type: ignore[index]


def piece_by_gpid(gpid: str) -> Piece:
    _ensure_loaded()
    return _PIECE_BY_GPID[gpid]   # type: ignore[index]


def total_pieces() -> int:
    return len(pieces())


# ──────────────────────────────────────────────────────────────────────────────
# Bijection dimensions
# ──────────────────────────────────────────────────────────────────────────────

def radix_list() -> List[int]:
    """Return list of radices (n_positions) for every piece, in canonical order."""
    return [p.n_positions for p in pieces()]


def state_space_size() -> int:
    """Total number of distinct states = ∏ p.n_positions for all pieces."""
    result = 1
    for r in radix_list():
        result *= r
    return result


def state_space_bits() -> float:
    import math
    return math.log2(state_space_size())


