"""
cna_ssc/engine/piece_registry.py

Canonical registry of all 2,468 pieces in CNAv2.1.0.vmod.

Generated from buildFile.xml.  The ORDER of entries in PIECES is the
canonical variable ordering for the mixed-radix bijection.  It must never
change once established — reordering invalidates all encoded states.

Each piece entry:
    name     – Vassal entryName  (human-readable, unique enough)
    gpid     – Vassal global piece ID (integer string)
    image    – Primary image filename
    cat      – Category controlling which locations are valid:
                 'ground_unit'     : any hex + off-map supply zones
                 'aircraft'        : airfield hexes + air zones
                 'airfield_naval'  : any hex (airfield/naval counters)
                 'marker'          : any hex (admin markers)
                 'tracker'         : Game Track zone only
                 'weather'         : Weather Track zone only
                 'label'           : fixed (labels don't move)
    proto    – Vassal prototype name

Valid locations per category are defined in get_valid_locations().
"""

